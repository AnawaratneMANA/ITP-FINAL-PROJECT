    public void FillDropDown(){
        //Filling the Menu Drop down in the first page
        DBHelper dbhelper = new DBHelper();
        ResultSet rs = dbhelper.SelectCustomerCustomDetails();
        //Removing the default vales in the drop downs
        cusCombo.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] {  }));
        //Looping through search results
        try {
             String id;
            while(rs.next()){
            id = rs.getString(Reception_COL1);
            cusCombo.addItem(id);
        }
        } catch (SQLException e){
            System.out.println("Something is wrong with a drop down");
        }
        
    }
