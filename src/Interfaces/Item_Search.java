    public static void search() {
        try {

            String qury = "";

            if (jRadioButton1.isSelected()) {
                qury = "select * from item where Description like '" + jTextField1.getText() + "%'";
            } else if (jRadioButton2.isSelected()) {
                qury = "select * from item where Department like '" + jTextField1.getText() + "%'";
            }
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            ResultSet rs = DBHelper.search(qury);
            while (rs.next()) {

                Vector v = new Vector();

                v.add(rs.getString("idItem"));
                v.add(rs.getString("Description"));
                v.add(rs.getString("Department"));
                v.add(rs.getString("Qty_type"));
                v.add(rs.getString("Cost"));
                v.add(rs.getString("Low_Stock_Remind"));
                v.add(rs.getString("expire_date"));
                v.add(rs.getString("Status_expire_date"));
                v.add(rs.getString("Qty"));
                df.addRow(v);
            }
        } catch (Exception e) {

            e.printStackTrace();
        }
    }
