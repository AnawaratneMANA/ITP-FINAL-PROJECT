    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            if (jTextField2.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter department !");
                jTextField2.grabFocus();
            } else {
                int k = JOptionPane.showConfirmDialog(this, "Are You Sure to Save Data !", "WARNING", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
                if (k == 0) {
                    ResultSet rs = DBHelper.search("select * from department where name='" + jTextField2.getText() + "'");
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "This department is already added !");
                    } else {
                        DBHelper.iud("insert into department (name) values('" + jTextField2.getText() + "')");
                        JOptionPane.showMessageDialog(this, "Successfully Saved !");
                        jTextField2.setText("");
                        jTextField2.grabFocus();
                        load_department();
                        Project.Items.load_department();
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

