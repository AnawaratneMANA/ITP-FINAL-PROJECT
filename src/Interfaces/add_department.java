    private void load_department() {
        try {
            ResultSet rs = DBHelper.search("select * from department");
            jComboBox1.removeAllItems();
            while (rs.next()) {
                jComboBox1.addItem(rs.getString("name"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
