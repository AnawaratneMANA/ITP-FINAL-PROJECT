    private void calculate_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculate_btActionPerformed
      Double basicSalary;
      Double otHourse;
      Double otRate;
      try{
         basicSalary = Double.parseDouble(basicsalary_input.getText());
         otHourse = Double.parseDouble(othours_input.getText());
         otRate = Double.parseDouble(otrate_input.getText());
      }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(this,"please enter only integer values","Error",JOptionPane.ERROR_MESSAGE);
           return;
      }
       
       Double netSalary = basicSalary + (otHourse * otRate);
             
       net_salary.setText(Double.toString(netSalary));
       
       submit_bt.setEnabled(true);
       salaryupdate_bt.setEnabled(true);
       salarydelete_bt.setEnabled(true);
    }//GEN-LAST:event_calculate_btActionPerformed

    public void nullValidationForSalary(){       
        if(!otrate_input.getText().equals("") && !othours_input.getText().equals("") && !basicsalary_input.getText().equals("") && !net_salary.getText().equals("")){
            submit_bt.setEnabled(true);
            salaryupdate_bt.setEnabled(true);
            salarydelete_bt.setEnabled(true);
            
        }else if(!otrate_input.getText().equals("") && !othours_input.getText().equals("") && !basicsalary_input.getText().equals("") && net_salary.getText().equals("")){           
            calculate_bt.setEnabled(true);
            salaryupdate_bt.setEnabled(false);
            //salarydelete_bt.setEnabled(false);
            submit_bt.setEnabled(false);
        }else{
            calculate_bt.setEnabled(false);
            submit_bt.setEnabled(false);
            salaryupdate_bt.setEnabled(false);
            //salarydelete_bt.setEnabled(false);
        }
    }
    
