    public void nullInputsValidation(){
        boolean status = false;
      
       if(Jobtitel == "Servicer"){
            if(!eid_input.getText().equals("") && !name_input.getText().equals("") && !address_input.getText().equals("") && !nic_input.getText().equals("") && !phone_input.getText().equals("") && !email_input.getText().equals("") && !Gender.equals("") && !Dob_input.getDate().equals(0)){
                status = true;                
            }else{
                status = false;
            }
        }else if(Jobtitel == "Manager"){
            if(!eid_input.getText().equals("") && !name_input.getText().equals("") && !address_input.getText().equals("") && !nic_input.getText().equals("") && !phone_input.getText().equals("") && !email_input.getText().equals("") && !Gender.equals("") && !Dob_input.getDate().equals("") && !password_input.getText().equals("")){
                status = true;                
            }else{
                status = false;
            }
        }else if(Jobtitel == "Driver"){
            if(!eid_input.getText().equals("") && !name_input.getText().equals("") && !address_input.getText().equals("") && !nic_input.getText().equals("") && !phone_input.getText().equals("") && !email_input.getText().equals("") && !Gender.equals("") && ! Dob_input.getDate().equals("") && !license_input.getText().equals("")){
                status = true;                
            }else{
                status = false;
            }
        }
        
        if(status){
            add_bt.setEnabled(true);
            update_bt.setEnabled(true);
            delete_bt.setEnabled(true);
        }else{
            add_bt.setEnabled(false);
            update_bt.setEnabled(false);
            delete_bt.setEnabled(false);
        }
        
    }
    
