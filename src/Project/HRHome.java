    private void employee_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_employee_tableMouseClicked
        DefaultTableModel tm =(DefaultTableModel)employee_table.getModel();
        
        String eid = tm.getValueAt(employee_table.getSelectedRow(),0).toString();
        String name = tm.getValueAt(employee_table.getSelectedRow(),1).toString();
        String phone = tm.getValueAt(employee_table.getSelectedRow(),2).toString();
        String email = tm.getValueAt(employee_table.getSelectedRow(),3).toString();
        String address = tm.getValueAt(employee_table.getSelectedRow(),4).toString();
        String gender = tm.getValueAt(employee_table.getSelectedRow(),5).toString();
        String nic = tm.getValueAt(employee_table.getSelectedRow(),6).toString();
        String dob = tm.getValueAt(employee_table.getSelectedRow(),7).toString();
        String jobtitel = tm.getValueAt(employee_table.getSelectedRow(),8).toString();
        String license = tm.getValueAt(employee_table.getSelectedRow(),9).toString();
        
        eid_input.setText(eid);
        name_input.setText(name);
        phone_input.setText(phone);
        email_input.setText(email);
        address_input.setText(address);
        nic_input.setText(nic);
        license_input.setText(license);
        
        
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Dob_input.setDate(sdf.parse(dob));
        } catch (ParseException ex) {
            Logger.getLogger(HRHome.class.getName()).log(Level.SEVERE, null, ex);
        }
           
        if(gender.equals("Male")){
            Gender = "Male";
            male.setSelected(true);
        }else if(gender.equals("Feale")){
            Gender = "Femal";
            female.setSelected(true);
        }
        
        if(jobtitel.equals("Servicer")){
            Jobtitel = "Servicer";
            service.setSelected(true);
        }else if(jobtitel.equals("Manager")){
            Jobtitel = "Manager";
            manager.setSelected(true);
            password_input.setEnabled(true);
             try{
                    DBHelper SQL = new DBHelper();
                    ResultSet rs = SQL.selectPassword(eid);
                    rs.next();
                    String password = rs.getString("password");
                     password_input.setText(password);
                } catch (SQLException e){
            
                }
             
        }else if(jobtitel.equals("Driver")){
            Jobtitel = "Driver";
            driver.setSelected(true);
            license_input.setEnabled(true);
        }
        
       
        
        
        add_bt.setEnabled(true);
        update_bt.setEnabled(true);
        delete_bt.setEnabled(true);
    }//GEN-LAST:event_employee_tableMouseClicked

