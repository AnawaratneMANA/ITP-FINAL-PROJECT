    private void CustomerTableclick(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CustomerTableclick
        //Table click event
        //Create Instance
        DefaultTableModel tableModel = (DefaultTableModel) CustomerTable.getModel();

        String cName = tableModel.getValueAt(CustomerTable.getSelectedRow(), 1).toString();
        int CID = (int) tableModel.getValueAt(CustomerTable.getSelectedRow(), 0);

        DBHelper dbhelper = new DBHelper();
        //retrieve kitchen requested food item  price
        ResultSet rs = dbhelper.SelectKitchenRequestedFoodItem(cName);
        //retrieve customer name
        ResultSet rsName = dbhelper.SelectCustomerName(cName);
        //retrive package name
        ResultSet rsPackage = dbhelper.SelectPckageName(CID);
        //retrive package price
        ResultSet rs2 = dbhelper.RoomPackagePrice(CID);
        //retrive No of days
        ResultSet rs5 = dbhelper.SelectNoOfDays(CID);
        //retrive package  discount
        ResultSet rs3 = dbhelper.SelectPackageDiscount(CID);
        //retrive bar price
        ResultSet rs4 = dbhelper.SelectBarPrice(CID);
        //retrive bar Discount
        ResultSet rs6 = dbhelper.SelectBarDiscount(CID);
        //retrive bar Total
        ResultSet rs7 = dbhelper.SelectBarTotal(CID);
        //retrive inventory requseted item price
        ResultSet rs8 = dbhelper.SelectInventoryRequestedItemPrice(CID);
        //retrive customer Phone
        ResultSet rs9 = dbhelper.SelectCustomerPhone(CID);
        //retrive customer Address
        ResultSet rs10 = dbhelper.SelectCustomerAdddress(CID);

        try {
            rs.next();
            String KitchenPrice = rs.getString("value");
            KitchenFood.setText( KitchenPrice);

            rs8.next();
            //System.out.println("1");
            String InventoryPrice = rs8.getString("total");
            double Iprice = Double.parseDouble(InventoryPrice);

            InventoryFood.setText(InventoryPrice);
            double Kprice = Double.parseDouble(KitchenPrice);
            //KitchenFood.setText(String.format("%.2f", Kprice));//Error - Order item table
            double RequestedTotal = Iprice + Kprice;
            String TotalPrice = String.valueOf(RequestedTotal);
            Rprice.setText(TotalPrice);
            rsName.next();

            String CustomerName = rsName.getString("CName");

            Cname.setText(CustomerName);
            NameLable.setText(CustomerName);
            rsPackage.next();

            String PackageName = rsPackage.getString("Name");

            Pname.setText(PackageName);
            PackageLable.setText(PackageName);
            rs2.next();

            String PackagePrice = rs2.getString("price");

            Pprice.setText(PackagePrice);
            rs5.next();

            String NoOfDay = rs5.getString("NoofDays");
            double days = Double.parseDouble(NoOfDay);

            NoOfDays.setText(NoOfDay);

            double pPrice = Double.parseDouble(PackagePrice);
            double Price = days * pPrice;
            String pTotalPrice = String.valueOf(Price);

            TotalPackagePrice.setText(pTotalPrice);

            rs3.next();

            String PackageDiscount = rs3.getString("Discount");
            double pDiscount = Double.parseDouble(PackageDiscount);

            packageDiscount = pDiscount;

            PDiscount.setText(PackageDiscount);
            rs4.next();

            String BarPrice = rs4.getString("totalPrice");

            Bprice.setText(BarPrice);
            rs6.next();

            String barDiscount = rs6.getString("Discount");

            BDiscount.setText(barDiscount);

            rs7.next();

            String BarTotal = rs7.getString("netAmount");
            double BPrice = Double.parseDouble(BarTotal);

            BTotal.setText(BarTotal);

            Double subTotal = RequestedTotal + Price + BPrice;
            String Subtotal = String.valueOf(subTotal);
            STotal.setText(Subtotal);

            double Ftotal = subTotal - pDiscount;
            String fTotal = String.valueOf(Ftotal);
            FinalTotal.setText(fTotal);
            finalTotal = Ftotal;
            System.out.println(finalTotal);

            String PaymentDEtailtotal = String.valueOf(finalTotal);
            Amount.setText(PaymentDEtailtotal);
            CAmount.setText(PaymentDEtailtotal);

            Fullname.setText(CustomerName);

            rs9.next();
            String PhoneNo = rs9.getString("phone_num");
            phoneNo.setText(PhoneNo);

            rs10.next();
            String add = rs10.getString("Address");
            Address.setText(add);

        } catch (SQLException e) {
            e.printStackTrace();
        }

        fillCustomertable();

    }//GEN-LAST:event_CustomerTableclick

