    private void UpdatePaymentDetails(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdatePaymentDetails
        //Validations
        if(transactionID.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please select Transaction!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Getting data from the text fields 
        int id = Integer.parseInt(transactionID.getText());        
        
        System.out.println(id);                
      
        if(CardButton2.isSelected()){
            String new_Cardname = CardName.getSelectedItem().toString();
        int NewCvvNo = Integer.parseInt(cvvNo.getText().trim());
        String NewcardNo = CardNo2.getText();
        int new_Year = Integer.parseInt(year.getText());
        int new_Month = Integer.parseInt(month.getText());
        //Calling the update method from the DBHelper
        boolean status = DBHelper.UpdateCardPaymentDetails(NewcardNo,NewCvvNo, new_Cardname,new_Year,new_Month,id);
        if (status == true){
           JOptionPane.showMessageDialog(this,"Item Updated successfully","Massage",JOptionPane.INFORMATION_MESSAGE); 
        } else {
            JOptionPane.showMessageDialog(this,"Something is wrong with update!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        }else if(CashButton1.isSelected()){
            double new_PaybleAmount = Double.parseDouble(PAmount.getText());
        double new_Balance = Double.parseDouble(Balance.getText());
        //Calling the update method from the DBHelper
        boolean status1 = DBHelper.UpdateCashPaymentDetails(new_PaybleAmount,new_Balance,id);
        if (status1 == true){
           JOptionPane.showMessageDialog(this,"Item Updated successfully","Massage",JOptionPane.INFORMATION_MESSAGE); 
        } else {
            JOptionPane.showMessageDialog(this,"Something is wrong with update!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        }
        fillTransactiontable();
        
    }//GEN-LAST:event_UpdatePaymentDetails

